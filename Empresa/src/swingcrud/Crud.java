	// method to save user to the db
	public void salvarPessoa() {
		try {
			Pessoa pessoa = criaPessoa();
			getFachadaPessoa().insere(pessoa);
		} catch (Exception ex) {
			Logger.getLogger(Crud.class.getName()).log(Level.SEVERE, null, ex);
		}
		// fetch();
	}

	protected FachadaPessoa getFachadaPessoa() {
		if (this.fachadaPessoa == null) {
			this.fachadaPessoa = new FachadaPessoa();
		}
		return this.fachadaPessoa;
	}

	// update the db
	public void alterarPessoa() {
		Pessoa criaPessoa = criaPessoa();
		try {
			getFachadaPessoa().atualiza(criaPessoa);
		} catch (SQLException e) {
			e.printStackTrace();
		}
		// fetch();
	}

	public void delete(String id) {
		try {
			getFachadaPessoa().remove(new Integer(id));
		} catch (NumberFormatException | SQLException e) {
			e.printStackTrace();
		}
		fetch();
	}

	// method to clear the txt fields
	private void clear() {
		txtNome.setText("");
		txtEndereco.setText("");
		txtTelefone.setText("");
		txtId.setText("");
	}

	// fetch
	private void fetch() {
		students.clear();
		try {
			List<Pessoa> pessoas = getFachadaPessoa().consultaPorNome(null);
			DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
			for (Pessoa p : pessoas) {

				Object[] row = new Object[4];
				row[0] = p.getNome();
				row[1] = p.getTelefone();
				row[2] = p.getEndereco();
				row[3] = p.getId();

				model.addRow(row);
			}

		} catch (Exception ex) {
			Logger.getLogger(Crud.class.getName()).log(Level.SEVERE, null, ex);
		}
	}

