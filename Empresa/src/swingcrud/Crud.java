	public Pessoa criaPessoa() {
		Pessoa pessoa = new Pessoa();
		String nome = txtNome.getText().trim();
		String endereco = txtEndereco.getText().trim();
		String telefone = txtTelefone.getText().trim();
//		String id = txtId.getText().trim();
		pessoa.setNome(nome);
		pessoa.setEndereco(endereco);
		pessoa.setTelefone(telefone);
		try {			
			pessoa.setId(new Integer(idLinhaSelecionada));
		} catch (Exception e) {
			// TODO: handle exception
		}
		return pessoa;
	}

	private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSaveActionPerformed
		try {
			Pessoa p = criaPessoa();
			salvarPessoa();
			DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
			Object[] row = new Object[4];
			row[0] = p.getNome();
			row[1] = p.getEndereco();
			row[2] = p.getTelefone();
			model.addRow(row);
			clear();
		} catch (Exception ex) {
			Logger.getLogger(Crud.class.getName()).log(Level.SEVERE, null, ex);
		}
	}

	private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnUpdateActionPerformed
		// TODO add your handling code here:
		String nome = txtNome.getText().trim();
		String telefone = txtEndereco.getText().trim();
		String endereco = txtTelefone.getText().trim();
		if (!nome.isEmpty() && !telefone.isEmpty() && !endereco.isEmpty()) {
			try {

				alterarPessoa();
				DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
				model.setRowCount(0);
				fetch();
				alert("Alterado, Rogério!");

			} catch (Exception ex) {
				Logger.getLogger(Crud.class.getName()).log(Level.SEVERE, null, ex);
			}
		} else {
			alert("Vai Atualizar Oq?", "Seleciona a linha la, Porra!");
		}
	}// GEN-LAST:event_btnUpdateActionPerformed

	// set the values of a row to the textfields
	private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tblStudentsMouseClicked
		// TODO add your handling code here:
		int i = tblStudents.getSelectedRow();
		TableModel model = tblStudents.getModel();
		txtNome.setText(model.getValueAt(i, 0).toString());
		txtEndereco.setText(model.getValueAt(i, 1).toString());
		txtTelefone.setText(model.getValueAt(i, 2).toString());
		setIdLinhaSelecionada((model.getValueAt(i, 3).toString()));
	}// GEN-LAST:event_tblStudentsMouseClicked

	// handles delete button action
	private void btnDelete1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDelete1ActionPerformed
		// TODO add your handling code here:
		int i = tblStudents.getSelectedRow();
		if (i >= 0) {
			int option = JOptionPane.showConfirmDialog(rootPane, "Vai remover mermo?", "Confirmação de Remoção",
					JOptionPane.YES_NO_OPTION);
			if (option == 0) {
				TableModel model = tblStudents.getModel();
				delete(getIdLinhaSelecionada());
				String id = model.getValueAt(i, 2).toString();
				if (tblStudents.getSelectedRows().length == 1) {
					delete(id);
					DefaultTableModel model1 = (DefaultTableModel) tblStudents.getModel();
					model1.setRowCount(0);
					fetch();
					clear();
				}
			}
		} else {
			alert("tem q selecionar a linha");
		}
	}// GEN-LAST:event_btnDelete1ActionPerformed

